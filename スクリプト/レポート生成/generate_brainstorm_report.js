// Helper: create a styled paragraph for dialogue
function dialogueLine(speakerName, speakerLabel, color, text, stageDirection) {
  const runs = [];
  if (stageDirection) {
    runs.push(
      new TextRun({
        text: `【${stageDirection}】`,
        color: "666666",
        italics: true,
        size: 20,
        font: "Yu Gothic",
      }),
      new TextRun({ text: "\n", break: 1 })
    );
  }
  runs.push(
    new TextRun({
      text: `${speakerName}（${speakerLabel}）：`,
      bold: true,
      color: color,
      size: 22,
      font: "Yu Gothic",
    }),
    new TextRun({
      text: text,
      size: 22,
      font: "Yu Gothic",
      color: "333333",
    })
  );
  return new Paragraph({
    children: runs,
    spacing: { after: 160, before: 80 },
    indent: { left: 200 },
  });
}

// Helper: Section heading
function sectionHeading(text, level) {
  return new Paragraph({
    children: [
      new TextRun({
        text: text,
        bold: true,
        color: NAVY,
        size: level === 1 ? 32 : level === 2 ? 28 : 24,
        font: "Yu Gothic",
      }),
    ],
    heading: level === 1 ? HeadingLevel.HEADING_1 : level === 2 ? HeadingLevel.HEADING_2 : HeadingLevel.HEADING_3,
    spacing: { before: 400, after: 200 },
    border: level <= 2
      ? {
          bottom: {
            color: ACCENT_BLUE,
            space: 4,
            style: BorderStyle.SINGLE,
            size: 8,
          },
        }
      : undefined,
  });
}

// Helper: bullet paragraph
function bulletParagraph(text, indent) {
  return new Paragraph({
    children: [
      new TextRun({
        text: text,
        size: 22,
        font: "Yu Gothic",
        color: "333333",
      }),
    ],
    bullet: { level: indent || 0 },
    spacing: { after: 80 },
  });
}

// Helper: normal paragraph
function normalParagraph(text, options = {}) {
  return new Paragraph({
    children: [
      new TextRun({
        text: text,
        size: options.size || 22,
        font: "Yu Gothic",
        color: options.color || "333333",
        bold: options.bold || false,
      }),
    ],
    spacing: { after: options.after || 120, before: options.before || 0 },
    alignment: options.alignment || AlignmentType.LEFT,
    indent: options.indent ? { left: options.indent } : undefined,
  });
}

// Helper: mixed-formatting paragraph
function mixedParagraph(runs, options = {}) {
  return new Paragraph({
    children: runs.map(
      (r) =>
        new TextRun({
          text: r.text,
          bold: r.bold || false,
          color: r.color || "333333",
          size: r.size || 22,
          font: "Yu Gothic",
          italics: r.italics || false,
        })
    ),
    spacing: { after: options.after || 120 },
    indent: options.indent ? { left: options.indent } : undefined,
  });
}

// Helper: Table cell
function createCell(text, options = {}) {
  return new TableCell({
    children: [
      new Paragraph({
        children: [
          new TextRun({
            text: text,
            size: options.size || 20,
            font: "Yu Gothic",
            bold: options.bold || false,
            color: options.color || (options.header ? WHITE : "333333"),
          }),
        ],
        alignment: options.alignment || AlignmentType.LEFT,
        spacing: { after: 40 },
      }),
    ],
    shading: {
      type: ShadingType.CLEAR,
      fill: options.header ? NAVY : options.shading || WHITE,
    },
    width: options.width
      ? { size: options.width, type: WidthType.PERCENTAGE }
      : undefined,
    verticalAlign: "center",
    margins: {
      top: 40,
      bottom: 40,
      left: 80,
      right: 80,
    },
  });
}

